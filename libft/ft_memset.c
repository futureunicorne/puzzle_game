#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	char	*s1;
	size_t	i;

	i = 0;
	if (c >= 0 || c <= 127)
	{
		s1 = (char*)s;
		while (i < n)
		{
			s1[i] = c;
			i++;
		}
	}
	return (s);
}
